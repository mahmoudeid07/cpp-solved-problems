#include <iostream>
using namespace std;

int main() {

int num = 10;
cout << num - 1 << " | " << num - 1 << " | " << num + 1 << "\n"; // 9 | 9 | 11
cout << num + 1 << " | " << num + 2 << " | " << num + 2 << "\n"; // 11 | 12 | 12
cout << num + 1 << " | " << num - 1 << " | " << num - 1 << "\n"; // 11 | 10 | 9

    return 0;
}
