#include <iostream>
using namespace std;

int main() {

//for output
for(int i = 0; i <= 10; i++) {
    cout << i << "\n";
}

cout << "\n";

//while output
int j = 0;
while(j <= 10) {
    cout << j << "\n";
    j++;
    
}

cout << "\n";

//do while output
int k = 0;
do {
    cout << k << "\n";
    k++;    
} while(k <= 10);

    return 0;
}
